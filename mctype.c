int microlena_isalnum(int c)
{
    return lci[c & 255];
}

int microlena_isupper(int c)
{
    return uci[c&255];
}

int microlena_tolower(int c)
{
    int d=lci[c&255];
    if (d) return d;
    return c;
}

int microlena_isvowel(int c)
{
	c=lci[c & 255];
    if (!c) return 0;
	return strchr("a\261e\352io\363uy\374\355@\341\351\372\375\366\344\343\365\342\353",c) ? 1 : 0;
}

int microlena_isalpha(int c)
{
    if (c >= '0' && c <= '9') return 0;
    return microlena_isalnum(c);
}
